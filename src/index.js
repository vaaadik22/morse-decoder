const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
   let result = expr.match(/.{1,10}/g).map(elem => elem.replace(/^0+/g, "").replace(/10/g, ".").replace(/11/g, "-").replace(/\*+/g, "!"));

   let string = result.map((element) => {
       for(let key in MORSE_TABLE){
           if(key == element){
               return MORSE_TABLE[key]
           } else if (element == "!"){
               return ""
           }
       }
   })
   return string.join('');
}

module.exports = {
    decode
}